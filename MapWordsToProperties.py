# Creating a dictionary
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}

# Printing the dictionary
print("Dictionary:", thisdict)

# Accessing specific elements
print("Brand:", thisdict["brand"])

# Getting the length of the dictionary
print("Length of the dictionary:", len(thisdict))

# Checking the type of the dictionary
print("Type of the dictionary:", type(thisdict))
